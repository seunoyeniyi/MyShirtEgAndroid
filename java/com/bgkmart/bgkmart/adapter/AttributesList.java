package com.fatima.fabric.adapter;

public class AttributesList {

    String id;
    String name;
    String options;
    String position;
    String visible;
    String variation;
    String is_visible;
    String is_variation;
    String is_taxonomy;
    String value;
    String label;

    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOptions() {
        return options;
    }

    public void setOptions(String options) {
        this.options = options;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getVisible() {
        return visible;
    }

    public void setVisible(String visible) {
        this.visible = visible;
    }

    public String getVariation() {
        return variation;
    }

    public void setVariation(String variation) {
        this.variation = variation;
    }

    public String getIs_visible() {
        return is_visible;
    }

    public void setIs_visible(String is_visible) {
        this.is_visible = is_visible;
    }

    public String getIs_variation() {
        return is_variation;
    }

    public void setIs_variation(String is_variation) {
        this.is_variation = is_variation;
    }

    public String getIs_taxonomy() {
        return is_taxonomy;
    }

    public void setIs_taxonomy(String is_taxonomy) {
        this.is_taxonomy = is_taxonomy;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
